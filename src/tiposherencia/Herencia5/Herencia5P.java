
package tiposherencia.Herencia5;

public class Herencia5P {


    
    //constructor
    public Herencia5P() {
    }
   
    
 //suma
public void Suma( int[] array ){ 


array[2] = array[0] + array[1] ;
    

}


//resta
public void Resta( int[] array ){ 


array[2] = array[0] - array[1] ;
    

}    
    
    
    
}
