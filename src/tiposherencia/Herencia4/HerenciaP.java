
package tiposherencia.Herencia4;


public class HerenciaP {

    
    //constructor
    public HerenciaP() {
    }
 
    
 //suma
public void Suma( int[] array ){ 


array[2] = array[0] + array[1] ;
    

}


//resta
public void Resta( int[] array ){ 


array[2] = array[0] - array[1] ;
    

}


//multiplicacion
public void Por( int[] array ){ 


array[2] = array[0] * array[1] ;
    

}
    
    
    
}
