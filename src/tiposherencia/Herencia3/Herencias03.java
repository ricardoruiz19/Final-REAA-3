
package tiposherencia.Herencia3;

public class Herencias03 {

   
    //constructor 
    public Herencias03() {
    
    }
   
        
//raiz     
public void Raiz( double[] array ){ 


array[ 2 ] = Math.sqrt( array[ 0 ] + array[ 1 ] );
    

}    
 
//logaritmo de la suma
public void Logaritmo( double[] array ){ 


array[ 2 ] = Math.log( array[ 0 ] + array[ 1 ] );
    

}
   
    
    
}
